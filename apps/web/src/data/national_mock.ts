/**
 * Convierte el dataset nacional en los `MapPoint` que consume `SpainMap`.
 *
 * `value` se mapea al precio medio de alquiler porque es la métrica dominante
 * en el tooltip del mapa principal. Cuando el usuario active otra capa, el
 * `LayerSwitcher` recalcula `value` con el indicador correspondiente.
 */
export function toMapPoints(
  entries: readonly NationalMunicipality[] = NATIONAL_MUNICIPALITIES,
  indicator: IndicatorId = 'rent_price'
): MapPoint[] {
  return entries.map((entry) => {
    const indicatorData = entry.indicators.find((i) => i.id === indicator);
    return {
      id: entry.id,
      name: entry.name,
      lat: entry.lat,
      lon: entry.lon,
      value: indicatorData?.value ?? 0,
      score: entry.score,
    };
  });
}

